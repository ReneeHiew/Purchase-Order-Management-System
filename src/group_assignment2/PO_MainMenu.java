    private void btnEnter1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEnter1ActionPerformed
        
        if (txtQuantity1.getText().isEmpty() || txtItemCode1.getText().isEmpty() || txtItemName1.getText().isEmpty() || txtUnitPrice1.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Empty field is detected for item entry details. " + "\nPlease make sure all item entry details are filled.", "Item Entry Detail Fields Detected Empty", JOptionPane.WARNING_MESSAGE);
            return;
        } else{
            String itemCode1 = txtItemCode1.getText();
            String itemName1 = txtItemName1.getText();
            double unitPrice1 = Double.parseDouble(txtUnitPrice1.getText());
            int quantity1 = Integer.parseInt(txtQuantity1.getText());
        
            CalculateTotal cal = new CalculateTotal(itemCode1, itemName1, unitPrice1, quantity1);
            double totalPrice1 = cal.totalPrice();

            txtTotal1.setText(Double.toString(totalPrice1));
        
            OrderItems obj1 = new OrderItems(itemCode1, itemName1, unitPrice1, quantity1, totalPrice1);
            orl2.addOrder(obj1);
        }
    }//GEN-LAST:event_btnEnter1ActionPerformed

