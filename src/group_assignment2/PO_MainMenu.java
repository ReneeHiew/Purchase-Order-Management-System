    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed

        String POid = txtPOid.getText();
        String PurID = txtPurID.getText();
        String Date = txtNeededBy.getText();
        String Req = txtReq.getText();
        
        List<OrderItems> listOrder = orl.getOrders();
        
        double sum = 0;
        for (OrderItems currentOrder : listOrder){
            sum += currentOrder.totalPrice();
        }

        CalculatePO cal1 = new CalculatePO();
        cal1.setSum(sum);
        txtTotalPur.setText(Double.toString(sum));
        txtTax.setText(Double.toString(cal1.calculateTotalTax()));
        txtTotalAmt.setText(Double.toString(cal1.calculateTotalAmount()));
        
        if (sum == 0 || cal1.calculateTotalTax() == 0 || cal1.calculateTotalAmount() == 0) {
            JOptionPane.showMessageDialog(null, "No item detected. Please enter the items to proceed check out", "Checkout Error", JOptionPane.ERROR_MESSAGE);

            listOrder.clear();
            DefaultTableModel dtm = (DefaultTableModel) tbOrders.getModel();
            dtm.setRowCount(0);
            txtTotalPur.setText("");
            txtTax.setText("");
            txtTotalAmt.setText("");
            return;
        }
        
        if (POid.length() != 6 || POid.isEmpty()|| PurID.length() != 4 || PurID.isEmpty() || Date.length() != 10 || Date.isEmpty() || Req.length() != 5 || Req.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please make sure all Purchase Order details is completed." + "\n* Purchase Order ID must be in 6 value *" + "\n* Date must be in 8 value *" + "\n* Purchaser ID must be in 4 value *" + "\n* Requisition ID must be in 5 value *", "Empty Field Deteacted", JOptionPane.ERROR_MESSAGE);
            return;
        }

        StringBuilder orderData = new StringBuilder();
        for (OrderItems order : orl.getOrders()) {
            orderData.append("\n" + POid.toUpperCase()).append("/").append(PurID.toUpperCase()).append("/").append(Date.toUpperCase())
                     .append("/").append(Req.toUpperCase()).append("/").append(order.getItemCode()).append("/").append(order.getItemName()).append("/").append(order.getUnitPrice())
                     .append("/").append(order.getQuantity()).append("/").append(order.gettotalPrice())
                     .append("/").append(sum).append("/").append(cal1.calculateTotalTax()).append("/").append(cal1.calculateTotalAmount())
                     .append(System.lineSeparator());
        }
        
        String userFilepath = "C:\\Users\\60125\\Documents\\OODJ Group Assignment\\user_log.txt";
        writeFile verify = new writeFile(userFilepath);
        
        if (!verify.verifyPurID(PurID)){
            JOptionPane.showMessageDialog(null, "Purchaser ID not found. Please try again.", "Invalid Purchase Manager ID", JOptionPane.WARNING_MESSAGE);
        } else{
            String PRfilepath = "C:\\Users\\60125\\Documents\\OODJ Group Assignment\\purchaseRequisition.txt";
            writeFile verifyPR = new writeFile(PRfilepath);

            if (!verifyPR.verifyReq(Req)){
                JOptionPane.showMessageDialog(null, "Requisition ID not found. Please try again.", "Invalid Requisition ID", JOptionPane.WARNING_MESSAGE);
            } else{
                String filepath = "C:\\Users\\60125\\Documents\\OODJ Group Assignment\\purchaseOrder.txt";

                String printData = orderData.toString();
                writeFile write = new writeFile(filepath);

                    if (write.verifyPOid(POid)){
                        JOptionPane.showMessageDialog(null, "Purchase Order ID existed. Please check Purchase Order ID.", "Purchase Order ID exits", JOptionPane.WARNING_MESSAGE);
                    } else{
                            if(write.appendOrder(printData)){
                                txtItemCode.setText("");
                                txtItemName.setText("");
                                txtUnitPrice.setText("");
                                txtQuantity.setText("");
                                txtTotal.setText("");
                                tbItem.clearSelection();
                                orl.getOrders().clear();
                                tbOrders.clearSelection();
                                JOptionPane.showMessageDialog(null, "Purchase Order successfully saved.", "Save Purchase Order: " + POid.toUpperCase(), JOptionPane.INFORMATION_MESSAGE);
                            }else {
                                JOptionPane.showMessageDialog(null, "Purchase Order Failed to save.", "Purchase Order Error Detacted", JOptionPane.ERROR_MESSAGE);
                            }
                    }
            }
        }
    }//GEN-LAST:event_btnSaveActionPerformed

